#! /usr/bin/zsh

mkdir $1

cd $1

subfinder -d $1 | tee sub.txt

echo $1 |assetfinder -subs-only | tee ast.txt

cat sub.txt ast.txt | sort -u >> subdomains.txt

cat subdomains.txt | httpx | tee domain1.txt

cat domain1.txt | waybackurls | tee url1.txt

cat url1.txt | /root/Tools/gf/gf/gf xss | tee xss.txt

echo "-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------"
echo "------------------------------------------------------------------------------Now KXSS Start---------------------------------------------------------------------------------------"
echo "-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------"

cat xss.txt | kxss
